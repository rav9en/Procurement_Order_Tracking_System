    private void btnDeleteSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSupplierActionPerformed
        int selectedRow = tblSupplier.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an supplier to delete!", "Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this supplier?",
                "Delete Confirmation",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            //Get the supplier ID from the selected row
            String supplierId = tblSupplier.getValueAt(selectedRow, 0).toString();

            //Delete the supplier
            boolean success = Supplier.deleteSupplierById(supplierId);

            if (success) {
                JOptionPane.showMessageDialog(this, "Supplier deleted successfully!", "Deletion Successful", JOptionPane.INFORMATION_MESSAGE);

                // Refresh the table data (you need to re-fetch data and update the table model)
                refreshSupplierTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete supplier. Please try again.", "Deletion Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteSupplierActionPerformed

