    private void btnEditSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditSupplierActionPerformed
        int selectedRow = tblSupplier.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a supplier to edit!", "Selection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String supplierId = tblSupplier.getValueAt(selectedRow, 0).toString();  
        String supplierName = tblSupplier.getValueAt(selectedRow, 1).toString();  
        String contactNumber = tblSupplier.getValueAt(selectedRow, 2).toString();  
        String email = tblSupplier.getValueAt(selectedRow, 3).toString();  
        String itemIdsStr = tblSupplier.getValueAt(selectedRow, 4).toString(); 
        List<String> itemIds = new ArrayList<>(Arrays.asList(itemIdsStr.split(","))); 
        String paymentMethod = tblSupplier.getValueAt(selectedRow, 5).toString();  
        String shippingMethod = tblSupplier.getValueAt(selectedRow, 6).toString();  
        String supplyCapacity = tblSupplier.getValueAt(selectedRow, 7).toString();  
        String leadTime = tblSupplier.getValueAt(selectedRow, 8).toString();  
        

        //Create a Supplier object for the selected supplier
        Supplier selectedSupplier = new Supplier(supplierId, supplierName, contactNumber, email, itemIds, paymentMethod, shippingMethod, supplyCapacity, leadTime);

        //Directed to edit supplier Interface
        Edit_Supplier editSupplier = new Edit_Supplier(selectedSupplier);
        editSupplier.setMainInterface(this);
        editSupplier.setVisible(true);

    }//GEN-LAST:event_btnEditSupplierActionPerformed

