    private void txtSearchSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchSupplierActionPerformed
        String searchText = txtSearchSupplier.getText().trim(); 
        boolean supplierFound = false; 
        DefaultTableModel model = (DefaultTableModel) tblSupplier.getModel();
        model.setRowCount(0); 

        //Find matching suppliers
        List<Supplier> suppliers = Supplier.searchSuppliers(searchText);

        for (Supplier supplier : suppliers) {
            model.addRow(new Object[]{
                supplier.getSupplierId(),
                supplier.getSupplierName(),
                supplier.getContactNumber(),
                supplier.getEmail(),
                String.join(", ", supplier.getItemIds()), // Assuming itemIds is a List, join them as a string
                supplier.getPaymentMethod(),
                supplier.getShippingMethod(),
                supplier.getSupplyCapacity(),
                supplier.getLeadTime()
            });
            supplierFound = true;
        }

        if (!supplierFound) {
            JOptionPane.showMessageDialog(null, "No supplier found with the input ID or Name.", "Supplier Not Found", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_txtSearchSupplierActionPerformed

